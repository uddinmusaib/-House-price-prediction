"""
app.py
------
Streamlit web app for the House Price Prediction project (AlgoHub ML Internship - Week 1).
Loads the trained Random Forest model and predicts house price from user inputs.
"""

import streamlit as st
import joblib
import numpy as np
import pandas as pd
import json

st.set_page_config(page_title="House Price Predictor", page_icon="🏠", layout="centered")

# ---- Load model & metadata ----
@st.cache_resource
def load_artifacts():
    model = joblib.load("model.pkl")
    zip_freq_map = joblib.load("zip_freq_map.pkl")
    with open("metrics.json") as f:
        metrics = json.load(f)
    return model, zip_freq_map, metrics


model, zip_freq_map, metrics = load_artifacts()

st.title("🏠 House Price Prediction System")
st.markdown(
    "**AlgoHub Software House — ML Internship | Week 1 Project**\n\n"
    "Predict a house's estimated sale price based on its bedrooms, bathrooms, "
    "size, and location (zip code)."
)

st.divider()

col1, col2 = st.columns(2)

with col1:
    beds = st.number_input("Number of Bedrooms", min_value=1, max_value=15, value=3, step=1)
    baths = st.number_input("Number of Bathrooms", min_value=1.0, max_value=10.0, value=2.0, step=0.5)

with col2:
    size = st.number_input("House Size (sq ft)", min_value=100.0, max_value=20000.0, value=1800.0, step=50.0)
    zip_options = sorted(zip_freq_map.keys())
    zip_code = st.selectbox("Zip Code", options=zip_options, index=0)

predict_btn = st.button("Predict Price", type="primary", use_container_width=True)

if predict_btn:
    zip_freq = zip_freq_map.get(zip_code, np.mean(list(zip_freq_map.values())))
    beds_baths_total = beds + baths

    X_input = pd.DataFrame(
        [[beds, baths, size, zip_freq, beds_baths_total]],
        columns=["beds", "baths", "size", "zip_code_freq", "beds_baths_total"],
    )

    prediction = model.predict(X_input)[0]

    st.success(f"### Estimated Price: ${prediction:,.0f}")
    st.caption(
        "This is an estimate generated by a Random Forest Regressor trained on "
        "historical housing data. Actual market prices may vary."
    )

st.divider()

with st.expander("📊 Model Performance (on test set)"):
    df_metrics = pd.DataFrame(metrics)
    st.dataframe(df_metrics, use_container_width=True, hide_index=True)
    st.caption("MAE / RMSE in dollars. Higher R² is better (closer to 1.0).")

st.caption("Built for the AlgoHub Machine Learning Internship Program — Week 1: House Price Prediction.")
